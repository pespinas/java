public class Main {
    public static void main(String[] args) {
        //Logicos
        boolean si = true;
        System.out.println(si);
        //Caracter
        char text = 'a';
        System.out.println(text);
        //Bytes
        byte b = 12;
        System.out.println(b);
        //Numero entero
        int num = 100;
        System.out.println(num);
        //Entero largo
        long numL = 100000;
        System.out.println(numL);
        //Entero corto
        short numS = 1;
        System.out.println(numS);
        //Numeros decimales
        float numDecimal = 1.02f;
        System.out.println(numDecimal);
        //Numero decimal m√°s preciso
        double numDoble = 10.14;
        System.out.println(numDoble);
    }
}